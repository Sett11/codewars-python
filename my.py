def read_barcode(s):
    f=lambda x,y:''.join(str(y[''.join('0' if j==' ' else '1' for j in x[i:i+7])]) for i in range(0,len(x),7))
    l,r=f(s[3:45],LEFT_HAND),f(s[50:-3],RIGHT_HAND)
    return f'{l[0]} {l[1:]} {r[:-1]} {r[-1]}'

