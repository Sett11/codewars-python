def fish(s):
    c,a,k=1,[],sorted([int(i) for i in s if i!='0'])
    while len(k):
        if(k[0])>c:
            break
        else:
            a.append(k.pop(0))
            if(sum(a)>=c*4):
                a=[sum(a)-c*4]
                c+=1
    return c

