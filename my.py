def count(a):
    q,u=sum([[(i,k) for k,p in enumerate(j) if p=='K'] for i,j in enumerate(a)],[])[0],set()
    f=lambda x,y:[(i,j) for i,j in [(x+1,y+2),(x-1,y-2),(x+2,y+1),(x-2,y-1),(x+1,y-2),(x+2,y-1),(x-1,y+2),(x-2,y+1)] if 0<=i<8 and 0<=j<8]
    for i in f(*q):
        u.update(f(*i))
    return len(u)

