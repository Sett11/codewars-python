def sort_nested_list(l):
    q=l.copy()
    n=len(l[0])
    m=len(l[0][0])
    a=[]
    r=[]
    l=sorted([k for i in l for j in i for k in j])
    i=0
    while i<len(l):
        a.append(l[i:i+m])
        i+=m
    i=0
    while i<len(a):
        r.append(a[i:i+n])
        i+=n
    return r if m else q

