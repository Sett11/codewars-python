def sq(a,x,y,m):
    for i in range(m-1):
        w,t=m-i,0
        for j in a[x:x+w]:
            t+=sum([int(k==' ') for k in j[y:y+w]])
            if t==w**2:
                return w

def _max(a):
    n,m=len(a),1
    for i in range(n):
        for j in range(n):
            x=sq(a,i,j,min(n-i,n-j))
            y=sq([p[::-1] for p in a][::-1],i,j,min(n-i,n-j))
            if x:
                for k in range(2,x+1):
                    m=max(k,m)
            if y:
                for k in range(2,y+1):
                    m=max(k,m)
    return m*m

